// Data Type Conversion in JavaScript;

// 01_String to number;

random = '2020';
random = Number('2020');

// 02_Boolean to number;

random = Number('Husein');

// parseInt method:

random = parseInt('20.2');

//parseFloat method:

random = parseFloat('20.3');

// 03_Number to string;

random = String(3);
random = String(3 * 3 * 2);

// 04_Boolean to string;

random = String(true);

// 05_Date to a string;

random = String(new Date());

// 06_Array to a string:

random = String([60, 1, 7, 30, 12, 365]);

// toString method:

random = true.toString();

console.log(random);
console.log(typeof random);
